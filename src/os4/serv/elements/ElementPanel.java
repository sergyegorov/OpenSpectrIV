    int XStep = 1,YStep = 1;
    Font LitleFont = new Font("Serif",Font.BOLD,9);
    Font TinyFont = new Font("Serif",Font.PLAIN,9);
    Font NormalFont = new Font("Serif",Font.BOLD,12);
    @Override
    public void paintComponent(Graphics g){
        try{
            Dimension dim = getSize();
            g.setColor(Color.white);
            g.fillRect(0, 0, dim.width, dim.height);
            
            int x_step = dim.width/18; XStep = x_step;
            int y_step = dim.height/10; YStep = y_step;
            
            boolean selected;
            for (ChElement chel : ElementTable.Elements) {
                int x = x_step*(chel.Col-1);
                int y = y_step*(chel.Row-1);
                if(SelectedCol == chel.Col && SelectedRow == chel.Row)
                    selected = true;
                else
                    selected = false;
                if(selected == false)
                    g.setColor(chel.getColorForType());
                else
                    g.setColor(Color.blue);
                g.setFont(NormalFont);
                g.fillRect(x, y, x_step, y_step);
                
                if(selected == true)
                    g.setColor(Color.white);
                else
                    g.setColor(Color.black);
                g.drawRect(x, y, x_step, y_step);
                g.drawString(chel.Name, x+2, y+15);
                g.setFont(LitleFont);
                
                String str = ""+chel.AtomNum;
                FontMetrics fm = g.getFontMetrics(LitleFont);
                int w = fm.stringWidth(str);
                g.drawString(str, x+x_step-w, y+15);
                
                //g.setColor(Color.darkGray);
                g.setFont(TinyFont);
                fm = g.getFontMetrics(TinyFont);
                str = chel.FullName+" ";
                do{
                    str = str.substring(0,str.length()-1);
                    w = fm.stringWidth(str);
                } while(w > x_step);
                g.drawString(str, x+x_step-1-w, y+y_step-1);
            }
        } catch(Exception ex){
            Log.log(Level.SEVERE,"Element panel...", ex);
        }
    }

