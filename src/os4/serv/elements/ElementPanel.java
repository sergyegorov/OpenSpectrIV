    int SelectedCol = -1;
    int SelectedRow = -1;
    public ChElement SelectedElement = null;
    private void formMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_formMouseClicked
        try{
            SelectedCol = evt.getX()/XStep+1;
            SelectedRow = evt.getY()/YStep+1;
            SelectedElement = null;
            for (ChElement Element : ElementTable.Elements) {
                if (Element.Col == SelectedCol && Element.Row == SelectedRow) {
                    SelectedElement = Element;
                    break;
                }
            }
            repaint();
            if(Listener != null)
                Listener.actionPerformed(null);
        }catch(Exception ex){
            Log.log(Level.SEVERE,"Elemenet table clic error",ex);
        }
    }//GEN-LAST:event_formMouseClicked


